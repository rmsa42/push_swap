#include "libft.h"

static int	len(unsigned int num)
{
	int	i;

	i = 0;
	if (num == 0)
		i++;
	while (num != 0)
	{
		num = num / 16;
		i++;
	}
	return (i);
}

int	ft_printhexa(unsigned int nbr)
{
	char	*hexa;

	hexa = "0123456789abcdef";
	if (nbr < 16)
		ft_putchar(*(hexa + nbr));
	else
	{
		ft_printhexa(nbr / 16);
		ft_printhexa(nbr % 16);
	}
	return (len(nbr));
}

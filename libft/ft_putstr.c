#include "libft.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (*s != '\0')
	{
		ft_putchar(*s++);
		i++;
	}
	return (i);
}
